    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try{
            Registry registro=LocateRegistry.getRegistry("127.0.0.1", 4567);
            RMIBD interfaz = (RMIBD) registro.lookup("rmi://localhost:4567/RMIBD");
            
            int cod;
            
            cod=Integer.parseInt(txtCodigo.getText());

            
            if(interfaz.BusLis_Cursos(cod).isEmpty()){
                
                JOptionPane.showMessageDialog(null,"no encontrado");
                
            }else{
                txtNombre.setText(interfaz.BusLis_Cursos(cod).get(0).getNombreCursos());
                txtHoras.setText(interfaz.BusLis_Cursos(cod).get(0).getHorasCursos());
                txtDocente.setText(interfaz.BusLis_Cursos(cod).get(0).getCodigoDocentes());
                txtIncidencias.setText(interfaz.BusLis_Cursos(cod).get(0).getIncidenciasCursos());


            }
        
        }catch(RemoteException | NotBoundException ex){
            JOptionPane.showMessageDialog(null, ex);
            
        }

    
    }//GEN-LAST:event_jButton1ActionPerformed

