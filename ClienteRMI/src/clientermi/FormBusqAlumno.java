    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try{
            Registry registro=LocateRegistry.getRegistry("127.0.0.1", 4567);
            RMIBD interfaz = (RMIBD) registro.lookup("rmi://localhost:4567/RMIBD");
            int cod;
            
            cod=Integer.parseInt(jTextField1.getText());

            
            if(interfaz.buscar_persona(cod).isEmpty()){
                
                JOptionPane.showMessageDialog(null,"no encontrado");
                
            }else{
                jTextField2.setText(interfaz.buscar_persona(cod).get(0).getNombre());
                jTextField3.setText(interfaz.buscar_persona(cod).get(0).getApellido());
                jTextField4.setText(interfaz.buscar_persona(cod).get(0).getDireccion());
                jTextField5.setText(interfaz.buscar_persona(cod).get(0).getEmail());
                jTextField7.setText(interfaz.buscar_persona(cod).get(0).getGrado());

            }
        
        }catch(RemoteException | NotBoundException ex){
            JOptionPane.showMessageDialog(null, ex);
            
        }
                                            

    
    }//GEN-LAST:event_jButton1ActionPerformed

